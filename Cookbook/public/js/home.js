var slideIndex0 = 0;
carousel();

function carousel() {
  var i;
  var x = document.getElementsByClassName("slider0-item");
  for (i = 0; i < x.length; i++) {
    x[i].style.display = "none";
  }
  slideIndex0++;
  if (slideIndex0 > x.length) {slideIndex0 = 1}
  x[slideIndex0-1].style.display = "block";
  setTimeout(carousel, 5000); // Change slide every 5 seconds
}
